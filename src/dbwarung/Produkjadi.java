    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("id produk");
        model.addColumn("id pemasok");
        model.addColumn("nama produk");
        model.addColumn("stock produk");
        model.addColumn("harga satuan");
        
        try{
            int no = 1;
            Dbwarung konek = new Dbwarung();
                Connection kon = konek.bukaKoneksi();
                Statement stat = kon.createStatement();
            String sql = "SELECT * FROM produk_jadi INNER JOIN pemasok ON produk_jadi.ID_PEMASOK = pemasok.ID_PEMASOK";
            java.sql.ResultSet res=stat.executeQuery(sql);
            while(res.next()){
                DateFormat dfto = new SimpleDateFormat("dd MMMM yyyy");
                DateFormat dffrom = new SimpleDateFormat("yyyy-MM-dd");  
                Date today = dffrom.parse(res.getString(6));
                String s = dfto.format(today);
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),s});
            }
            jTable1.setModel(model);
        }catch(Exception e){
            
        }
    }
    public void load_pemasok_masuk() {
        try{
            int no = 1;
            Dbwarung konek = new Dbwarung();
                Connection kon = konek.bukaKoneksi();
                Statement stat = kon.createStatement();
            String sql = "SELECT * FROM pemasok";
            java.sql.ResultSet res=stat.executeQuery(sql);
            while(res.next()){
                cmbSupp.addItem(res.getString(1));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }
    
