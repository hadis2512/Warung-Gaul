    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(id_produk.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Kode Barang can't be empty", "WARNING", JOptionPane.WARNING_MESSAGE);

                nama_produk.setText("");

                harga_satuan.setText("");
                stock_produk.setText("");
                id_produk.requestFocus();
            }else if(cmbSupp.getSelectedItem().equals("")){
                JOptionPane.showMessageDialog(this, "Stock can't be empty", "WARNING", JOptionPane.WARNING_MESSAGE);
               
                id_produk.setText("");
                nama_produk.setText("");
                harga_satuan.setText("");
                stock_produk.setText("");
                
            }
            else if(nama_produk.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Nama Barang can't be empty", "WARNING", JOptionPane.WARNING_MESSAGE);

                id_produk.setText("");

                harga_satuan.setText("");
                stock_produk.setText("");
                nama_produk.requestFocus();

            }else if(harga_satuan.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Harga can't be empty", "WARNING", JOptionPane.WARNING_MESSAGE);

                id_produk.setText("");
                nama_produk.setText("");
                stock_produk.setText("");
                harga_satuan.requestFocus();
            }else if(stock_produk.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Harga can't be empty", "WARNING", JOptionPane.WARNING_MESSAGE);

                id_produk.setText("");
                nama_produk.setText("");
                harga_satuan.setText("");
                stock_produk.requestFocus();
            }
            else{
                Dbwarung konek = new Dbwarung();
                Connection kon = konek.bukaKoneksi();
                Statement stat = kon.createStatement();
                String sql = "SELECT * from pemasok,produk_jadi where produk_jadi.ID_PEMASOK = '"+cmbSupp.getSelectedItem()+"' AND pemasok.ID_PEMASOK = produk_jadi.ID_PEMASOK";
                java.sql.ResultSet res = stat.executeQuery(sql);
                res.next();
                String datez = tahun.getSelectedItem().toString() +"-"+ bulan.getSelectedItem().toString() +"-"+ tanggal.getSelectedItem().toString();
                String query = "INSERT INTO `produk_jadi`(`ID_PRODUK`, `ID_PEMASOK`, `NAMA_PRODUK`,`STOK`,`HARGA_SATUAN`,`TANGGAL`) VALUES ('"+id_produk.getText()+"','"+cmbSupp.getSelectedItem().toString()+"','"+nama_produk.getText()+"','"+stock_produk.getText()+"','"+harga_satuan.getText()+"','"+datez+"')";
                int rows = stat.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Data Berhasil di Input", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

